
ll powermod(ll a, ll b, ll m)
 {
     if (b == 0) return 1;
     ull k = powermod(a, b / 2, m);
     k = k * k;
     k %= m;
     if (b & 1) k = (k * a) % m;
     return k;
}
 
bool isPrime(ull n, int iter = 5)
 {
     if (n < 4) return n == 2 || n == 3;
    if (n % 2 == 0 || n % 3 == 0) return false;
     for (int i = 0; i < iter; i++)
     {
        ull a = 2 + rand() % (n - 3);
         if (powermod(a, n - 1, n) != 1) return false;
    }
     return true;
}
